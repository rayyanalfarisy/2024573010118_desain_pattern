package Praktikum_3.bagian_4.Latihan_2_Overriding;

public class Matematika {
    //overloading 1
    public int tambah(int a,int b){
        return a + b;
    }
    //overloading 2
    public int tambah(int a,int b,int c){
        return a + b + c;
    }
    //overloading 3
    public double tambah(double a,double b){
        return a + b;
    }
}
